# ───────────────────────────────────────────────────────────────────────────────
# FE solver
# ───────────────────────────────────────────────────────────────────────────────

def fe_solve(load_vector):
    """
    Solve the 2D truss FE problem for a given load vector.

    Parameters
    ----------
    load_vector : array, shape [20]
        Applied force (x-direction) at each of the 20 ChosenNodes.

    Returns
    -------
    displacements : array, shape [nNodes, 2]
        Nodal displacements [(u_x1, u_x2), ...] for each node.
    stresses : array, shape [nElements]
        Axial stress in each bar element (Pa).
    """
    x1 = NODAL_POSITIONS[:, 0]   # x1-coordinates (0-indexed)
    x2 = NODAL_POSITIONS[:, 1]   # x2-coordinates

    nNodes    = len(x1)
    nDofs     = 2 * nNodes
    conn      = CONNECTIVITIES_1IDX - 1   # Convert to 0-indexed
    nElements = len(conn)

    # ── Build global stiffness matrix ───────────────────────────────────────
    K_global = np.zeros((nDofs, nDofs))
    F_global = np.zeros(nDofs)
    R_store  = []    # Store rotation matrices for stress recovery
    L_store  = []    # Store element lengths

    for e in range(nElements):
        n1, n2 = conn[e, 0], conn[e, 1]

        # Nodal coordinates
        x1e = np.array([x1[n1], x1[n2]])
        x2e = np.array([x2[n1], x2[n2]])

        # Orientation vector: MATLAB uses [Δx2, Δx1] order for atan2
        dv = np.array([x2e[1] - x2e[0], x1e[1] - x1e[0]])
        angle  = np.arctan2(dv[0], dv[1])
        length = np.linalg.norm(dv)
        L_store.append(length)

        # Axial stiffness
        k = E_MODULUS * AREA / length
        K_axial = k * np.array([[1, -1], [-1, 1]])

        # Rotation matrix (2×4)
        c = np.cos(angle)
        s = np.sin(angle)
        R = np.array([[c, s, 0, 0],
                      [0, 0, c, s]])
        R_store.append(R)

        # Global element stiffness
        K_elem = R.T @ K_axial @ R   # 4×4

        # DOFs: [n1_x, n1_y, n2_x, n2_y]
        dofs = np.array([2*n1, 2*n1+1, 2*n2, 2*n2+1])
        for i, di in enumerate(dofs):
            for j, dj in enumerate(dofs):
                K_global[di, dj] += K_elem[i, j]

    # ── Assemble force vector ────────────────────────────────────────────────
    # Loads applied in x-direction (DOF 1 in MATLAB = index 0 in 0-indexed)
    for i, node_1idx in enumerate(CHOSEN_NODES_1IDX):
        dof = 2 * (node_1idx - 1)   # x-DOF (0-indexed)
        F_global[dof] = load_vector[i]

    # ── Apply Dirichlet BCs (zero rows/cols, set diagonal to 1) ─────────────
    K = K_global.copy()
    for row in DIRICHLET_BCS:
        node_1idx = int(row[0])
        dof_1idx  = int(row[1])
        val       = row[2]
        dof = 2 * (node_1idx - 1) + (dof_1idx - 1)   # convert to 0-indexed
        K[dof, :] = 0
        K[dof, dof] = 1
        F_global[dof] = val

    # ── Solve K u = F ───────────────────────────────────────────────────────
    u = np.linalg.solve(K, F_global)   # shape [nDofs]

    # ── Reshape displacements ────────────────────────────────────────────────
    displacements = u.reshape(nNodes, 2)   # [nNodes, 2] — (u_x1, u_x2)

    # ── Compute element stresses ─────────────────────────────────────────────
    stresses = np.zeros(nElements)
    for e in range(nElements):
        n1, n2 = conn[e, 0], conn[e, 1]
        dofs_e  = np.array([2*n1, 2*n1+1, 2*n2, 2*n2+1])
        u_e     = u[dofs_e]
        # Strain: local elongation / original length
        local_u = R_store[e] @ u_e   # [2] — local axial displacements
        strain  = (local_u[1] - local_u[0]) / L_store[e]
        stresses[e] = E_MODULUS * strain

    return displacements, stresses


# ───────────────────────────────────────────────────────────────────────────────
# Plotting helper
# ───────────────────────────────────────────────────────────────────────────────

def plot_structure(ax, load_vector, label, scale_factor=SCALE_FACTOR):
    """
    Plot original and deformed Eiffel Tower structure for one load sample.

    Parameters
    ----------
    ax          : matplotlib Axes
    load_vector : shape [20] — applied forces at 20 ChosenNodes
    label       : title string (e.g. 'Sample 0: Survives')
    scale_factor: multiplier on displacements for visualisation
    """
    conn = CONNECTIVITIES_1IDX - 1   # 0-indexed connectivity
    x1   = NODAL_POSITIONS[:, 0]
    x2   = NODAL_POSITIONS[:, 1]

    # Solve FE
    displacements, stresses = fe_solve(load_vector)

    # Displaced nodal positions
    x1_new = x1 + scale_factor * displacements[:, 0]
    x2_new = x2 + scale_factor * displacements[:, 1]

    # Plot original structure (black solid lines + nodes)
    p1 = None
    for e in range(len(conn)):
        n1, n2 = conn[e, 0], conn[e, 1]
        p1, = ax.plot([x1[n1], x1[n2]], [x2[n1], x2[n2]], 'k-', lw=0.8)
    ax.plot(x1, x2, 'ko', ms=2)

    # Plot displaced structure (red dashed)
    p2 = None
    for e in range(len(conn)):
        n1, n2 = conn[e, 0], conn[e, 1]
        p2, = ax.plot([x1_new[n1], x1_new[n2]], [x2_new[n1], x2_new[n2]],
                      'r--', lw=0.8)

    # Applied loads as blue quiver arrows (scaled for visibility)
    chosen_0idx = CHOSEN_NODES_1IDX - 1
    qx = NODAL_POSITIONS[chosen_0idx, 0]
    qy = NODAL_POSITIONS[chosen_0idx, 1]
    ax.quiver(qx, qy, load_vector / 5e5, np.zeros(20),
              scale=1, scale_units='xy', angles='xy', color='b', width=0.005)

    ax.set_xlabel('x1 (m)')
    ax.set_ylabel('x2 (m)')
    ax.set_title(label, fontsize=11)
    ax.legend([p1, p2], ['original structure', 'displaced structure'],
              loc='upper left', fontsize=8)
    ax.grid(True, alpha=0.3)
    ax.set_aspect('equal')

    max_stress = np.max(np.abs(stresses))
    print(f"  Max |stress| = {max_stress/1e6:.1f} MPa  "
          f"({'FAILS' if max_stress > YIELD_STRESS else 'SURVIVES'})")


